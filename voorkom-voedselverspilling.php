				<p>Op verpakte producten staat meestal hoe je het product het best kunt bewaren. Zet producten die kort houdbaar zijn vooraan. Dit voorkomt dat deze onopgemerkt over the houdsbaarheidsdatum gaan.</p>
				<p>Door groente en fruit goed te bewaren, gaan deze langer mee. Bananen moet nooit in de koelkast bewaard worden. Tropisch fruit, tomaten, paprika en aubergine kan je beter ook buiten de koelkast bewaren. Deze producten zijn het best te bewaren bij 7 graden, maar dat is te warm voor de meeste producten.</p>
				<p>Veel mensen gooien een product die over datum zijn weg. Vaak zijn deze producten nog prima te eten. Dit komt door verwarring van de houdbaarheidsdatum. Er zijn twee soorten houdbaarheidsdata, TGT en THT.</p>
				<p>TGT betekent 'te gebruiken tot'. Deze staat op producten die kort houdbaar zijn. Deze moeten weggegooid worden waneer de TGT is verstreken. Dit is niet het geval bij THT. THT betekend 'ten minste houdbaar tot'. De fabrikant gegarandeerd dat het product tot die datum goed is. Meestal is dit na die datem nog steeds goed. Koeken in een ongeopende verpakking kunnen bijvoorbeeld nog een half jaar na de THT goed zijn.
